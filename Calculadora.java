package exercicio4;

public class Calculadora {
// atributos
	double Numero1;
	double Numero2;
	
	//Metodo
	public double Somar(){
		//ira Somar os numeros do (proximo codigo)
		return this.Numero1+this.Numero2;
	}
	public double Dividir(){
		//ira Dividir os numeros do (proximo codigo)
		return this.Numero1/this.Numero2;
	}
	public double Subtrair(){
		//ira Subitrair os numeros do (proximo codigo)
		return this.Numero1-this.Numero2;
	}
	public double Multiplicar(){
		//ira Multiplicar os numeros do (proximo codigo)
		return this.Numero1*this.Numero2;
	}
	
		
		
	}

